package Modelos;

/**
 *
 * @author Franciso
 */
public class usuario_roles {
    int id;
    int id_rol;
    int id_usuario;
    public usuario_roles(){
        
    }
    public usuario_roles(int id, int id_rol, int id_usuario) {
        this.id = id;
        this.id_rol = id_rol;
        this.id_usuario = id_usuario;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_rol() {
        return id_rol;
    }

    public void setId_rol(int id_rol) {
        this.id_rol = id_rol;
    }

    public int getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(int id_usuario) {
        this.id_usuario = id_usuario;
    }
    
}
